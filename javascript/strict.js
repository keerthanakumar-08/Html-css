let currentAge = 25;

console.log(currentAge == 25);
console.log(currentAge == '25');


// Strict comparision - check value and type

console.log(currentAge === 25);
console.log(currentAge === '25');


console.log(currentAge !== 25);  // integer 25 is equal so return false
console.log(currentAge !== '25');// string 25 is not equal so return True